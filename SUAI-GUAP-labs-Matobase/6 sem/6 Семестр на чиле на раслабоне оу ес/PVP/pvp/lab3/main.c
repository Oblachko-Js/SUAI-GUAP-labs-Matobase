#include "stm32f3xx.h"
#include <stdio.h>
#include <stdint.h>

// ?????????? ??????????? ??????????? NOP ?? CMSIS
#undef __NOP
#include "core_cm4.h"

// ??????? ??? ?????? ? ??????
#define SET_BIT(REG, BIT)     ((REG) |= (BIT))
#define CLEAR_BIT(REG, BIT)   ((REG) &= ~(BIT))

// ????????? SystemCoreClock ??? extern
extern uint32_t SystemCoreClock;

// ??????? ????????? ??????????? SystemCoreClockUpdate
// (??? ??? ???? ? system_stm32f3xx.c)

void delay(volatile uint32_t count) {
    while(count--) __NOP();
}

int main(void) {
    // 1. ???????? HSI (?????????? ????????? 8 ???)
    SET_BIT(RCC->CR, RCC_CR_HSION);
    // ???? ???? HSI ???????????????
    while((RCC->CR & RCC_CR_HSIRDY) == 0) {}
    
    // 2. ??????????? ????? FLASH
    FLASH->ACR = FLASH_ACR_LATENCY_1;
    
    // 3. ????????? PLL ?? 28 ??? = 8 ??? (HSI) / 2 * 7
    CLEAR_BIT(RCC->CR, RCC_CR_PLLON); // ????????? PLL ????? ??????????
    
    // PLLSRC = HSI/2, PLLCLK = PLLSRC * 7
    RCC->CFGR |= (RCC_CFGR_PLLSRC_HSI_DIV2 | RCC_CFGR_PLLMUL7);
    
    SET_BIT(RCC->CR, RCC_CR_PLLON); // ???????? PLL
    // ???? ?????? ? ???????????? PLL
    while((RCC->CR & RCC_CR_PLLRDY) == 0) {}
    
    // 4. ????????????? ?? PLL
    RCC->CFGR |= RCC_CFGR_SW_PLL;
    // ??????? ????????????
    while ((RCC->CFGR & RCC_CFGR_SWS) != RCC_CFGR_SWS_PLL) {}
    
    // 5. ????????? ??????? (?????????? ??????????? ??????? ?? CMSIS)
    SystemCoreClockUpdate();
    printf("SystemCoreClock = %u Hz\n", (unsigned)SystemCoreClock);
    
    // 6. ????????? GPIO
    // ???????? ???????????? GPIOA
    SET_BIT(RCC->AHBENR, RCC_AHBENR_GPIOAEN);
    
    // ??????????? PA5 ? PA4 ??? ??????
    GPIOA->MODER |= (GPIO_MODER_MODER5_0 | GPIO_MODER_MODER4_0);
    // PA5 - open-drain
    GPIOA->OTYPER |= GPIO_OTYPER_OT_5;
    // ???????? PA5 ? ???????
    GPIOA->PUPDR |= GPIO_PUPDR_PUPDR5_0;
    
    // 7. ???????? ????
    while(1) {
        // ???????? PA5 ? PA4
        GPIOA->BSRR = (GPIO_BSRR_BS_5 | GPIO_BSRR_BS_4);
        delay(1000000);
        
        // ????????? PA5 ? PA4
        GPIOA->BRR = (GPIO_BRR_BR_5 | GPIO_BRR_BR_4);
        delay(1000000);
    }
}