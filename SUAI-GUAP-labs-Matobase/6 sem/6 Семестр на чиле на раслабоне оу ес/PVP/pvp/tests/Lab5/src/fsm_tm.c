/* ????????? ???????? ?????????? */
#define MTCTRL_RXCMD    0  // ???????? ??????? ???????
#define MTCTRL_PROC     1  // ?????? ??
#define MTCTRL_RDFRAM   2  // ?????? FRAM
#define MTCTRL_WRFRAM   3  // ?????? FRAM
#define MTCTRL_UARTTR   4  // ???????? UART

/* ????????? ???????? ???????? */
#define FSMST_Q0        0  // ????????? ?????????
#define FSMST_Q1        1  // ???????? ?????? ?? ????? ??????
#define FSMST_Q2        2  // ???????? ????? ? ???????? ???? 'a'
#define FSMST_Q3        3  // ?????????? ??????

static uint32_t curraddrtm = 0;  // ??????? ????????? ???????
static uint32_t nextaddrtm = 0;  // ????????? ????????? ???????

// ?????????? ????????? ???????? ???????? ??
static char mtstatectrl = MTCTRL_RXCMD;   
// ?????????? ????????? ???????? ??
static char fsmstate = FSMST_Q0;

int ProcTm(uint8_t rsymb, uint8_t* wrsymb) {
    static int stepcnt = 0;
    static int a_count = 0;  // ??????? ????????? 'a'
    *wrsymb = rsymb;
    
    switch(fsmstate) {
        case FSMST_Q0:  // ?????????????
            stepcnt = 1;
            nextaddrtm = 0;  // ???????? ? ?????? ?????
            a_count = 0;     // ?????????? ???????
            fsmstate = FSMST_Q1;
            break;
            
        case FSMST_Q1:  // ????? ????? ??????
            if(rsymb == 0 || rsymb == 0xFF) {  // ?????? ?????? - ????? ??????
                nextaddrtm = curraddrtm - 1;   // ???????? ???????? ?????
                fsmstate = FSMST_Q2;
            } else {
                nextaddrtm = curraddrtm + 1;   // ?????????? ???????? ??????
            }
            break;
            
        case FSMST_Q2:  // ????? ? ???????? ???? ????????? 'a'
            if(rsymb == 'a' && a_count < 2) {
                *wrsymb = '_';  // ??????? 'a'
                a_count++;
                if(a_count >= 2) {
                    fsmstate = FSMST_Q3;  // ????? ??? 'a' - ?????????
                }
            }
            nextaddrtm = curraddrtm - 1;  // ?????????? ???????? ?????
            break;
            
        case FSMST_Q3:  // ?????????? ??????
            stepcnt = -1;  // ?????? ?????????
            break;
    }
    
    if(stepcnt != -1) {
        stepcnt++;
    }
    return stepcnt;
}

void ProcessFsmTm(void) {
    static int steptm; 
    static uint8_t rdsymbol;
    static uint8_t wrsymbol;
    static uint8_t aTrbuf[32];
    uint8_t* pRxbuf = 0;
    
    switch (mtstatectrl) {
        case MTCTRL_RXCMD: // ???????? ??????? ???????
            if(GetMessage(MSG_TM1STRT, (void*)&pRxbuf)) {
                nextaddrtm = 0;  // ???????? ? ?????? ?????
                steptm = 1000;   // ???????????? ????? ????? (?? ?????? ??????)
                mtstatectrl = MTCTRL_RDFRAM;
                fsmstate = FSMST_Q0;  // ????????? ?????????
                ResetTimer(TM1_TIMER);
            }
            break;
            
        case MTCTRL_PROC: // ?????????? ???? ??
            if(GetTimer(TM1_TIMER) >= 1000) {  // ???? 1 ???????
                ResetTimer(TM1_TIMER);
                int curstep = ProcTm(rdsymbol, &wrsymbol);
                
                if(curstep == -1) {  // ?????????? ??????
                    sprintf((char*)aTrbuf, "Done. Removed 2 last 'a'\n");
                    steptm = -1;
                    mtstatectrl = MTCTRL_UARTTR;
                } 
                else if(wrsymbol == rdsymbol) {
                    mtstatectrl = MTCTRL_RDFRAM;  // ?????????? ??????
                } 
                else {
                    mtstatectrl = MTCTRL_WRFRAM;  // ????? ???????? ?????????
                }
            }
            break;
            
        case MTCTRL_RDFRAM:  // ?????? ?? FRAM
            pRxbuf = (uint8_t*)i2cFRAM_rd(nextaddrtm, 1);
            if((uint32_t)pRxbuf > 0) {
                rdsymbol = pRxbuf[0];
                curraddrtm = nextaddrtm;
                mtstatectrl = MTCTRL_PROC;
            }
            break;
            
        case MTCTRL_WRFRAM:  // ?????? ? FRAM
            if((uint32_t)i2cFRAM_wr(curraddrtm, &wrsymbol, 1) > 0) {
                mtstatectrl = MTCTRL_RDFRAM;  // ?????????? ??????
            }
            break;
            
        case MTCTRL_UARTTR:  // ???????? ??????????
            if(uart_transmit(aTrbuf, strlen((char*)aTrbuf)) == 0) {
                if(steptm == -1) {
                    mtstatectrl = MTCTRL_RXCMD;  // ?????? ? ????? ???????
                }
            }
            break;
    }
}