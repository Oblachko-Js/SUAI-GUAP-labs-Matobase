/* ????????? ?????????? ?????? ?? */
#define MT2CTRL_RXCMD    0  // ???????? ??????? ???????
#define MT2CTRL_PROC     1  // ?????? ??
#define MT2CTRL_RDFRAM   2  // ?????? FRAM
#define MT2CTRL_WRFRAM   3  // ?????? FRAM
#define MT2CTRL_UARTTR   4  // ???????? UART

/* ????????? ?????? ?? */
#define FSMST2_Q0        0  // ????????? ?????????
#define FSMST2_Q1        1  // ????? ?????? ??????
#define FSMST2_Q2        2  // ?????? ?????? ?????? ?? 'x'

static uint32_t curr_addr_tm2 = 0;  // ??????? ????? ?????
static uint32_t next_addr_tm2 = 0;  // ????????? ????? ?????
static int iteration_count = 0;     // ??????? ????????

// ?????????? ????????? ?????????? ?????? ??
static char mt2_state_ctrl = MT2CTRL_RXCMD;
// ?????????? ????????? ?????? ??
static char fsm2_state = FSMST2_Q0;

int ProcTm2(uint8_t rsymb, uint8_t* wrsymb, uint8_t* next_symb) {
    static uint8_t prev_symb = 0;
    *wrsymb = rsymb;
    
    switch(fsm2_state) {
        case FSMST2_Q0:  // ????????? ?????????
            next_addr_tm2 = 0;
            fsm2_state = FSMST2_Q1;
            prev_symb = 0;
            break;
            
        case FSMST2_Q1:  // ????? ?????? ??????
            if(rsymb == '(') {
                prev_symb = rsymb;
                next_addr_tm2 = curr_addr_tm2 + 1;
            } 
            else if(rsymb == ')' && prev_symb == '(') {
                // ????? ?????? ?????? - ???????? ?? ?? 'x'
                *wrsymb = 'x';
                *next_symb = 'x';
                fsm2_state = FSMST2_Q2;
                return 2; // ????? ???????? ??? ???????
            }
            else {
                prev_symb = rsymb;
                next_addr_tm2 = curr_addr_tm2 + 1;
            }
            break;
            
        case FSMST2_Q2:  // ?????? ?????? ??????
            *wrsymb = 'x';
            fsm2_state = FSMST2_Q1;
            prev_symb = 0;
            next_addr_tm2 = curr_addr_tm2 + 1;
            break;
    }
    
    iteration_count++;
    return 1;
}

void ProcessFsmTm2(void) {
    static int step_tm2; 
    static uint8_t rd_symbol;
    static uint8_t wr_symbol;
    static uint8_t next_symbol;
    static uint8_t aTrbuf[32];
    uint8_t* pRxbuf = 0;
    static uint16_t* pKeyStatus;
    
    // ???????? ?? ?????????? ?? ?????? K2
    if(GetMessage(MSG_KEYPRESSED, (void*)&pKeyStatus)) {
        uint16_t key_status = *pKeyStatus;
        if(key_status & (1 << 1)) { // ?????? K2 (??? 1)
            if(mt2_state_ctrl == MT2CTRL_PROC) {
                sprintf((char*)aTrbuf, "MT2 stopped by K2. Iterations: %d\n", iteration_count);
                mt2_state_ctrl = MT2CTRL_UARTTR;
            }
        }
    }
    
    switch (mt2_state_ctrl) {
        case MT2CTRL_RXCMD: // ???????? ??????? ???????
            if(GetMessage(MSG_TM2STRT, (void*)&pRxbuf)) {
                next_addr_tm2 = 0;
                step_tm2 = 1000;   // 1 ??????? ?? ????????
                mt2_state_ctrl = MT2CTRL_RDFRAM;
                fsm2_state = FSMST2_Q0;
                iteration_count = 0;
                ResetTimer(TM2_TIMER);
            }
            break;
            
        case MT2CTRL_PROC: // ?????????? ???? ??
            if(GetTimer(TM2_TIMER) >= step_tm2) {
                ResetTimer(TM2_TIMER);
                int changes = ProcTm2(rd_symbol, &wr_symbol, &next_symbol);
                
                if(changes == 1) {
                    if(wr_symbol == rd_symbol) {
                        mt2_state_ctrl = MT2CTRL_RDFRAM;
                    } else {
                        mt2_state_ctrl = MT2CTRL_WRFRAM;
                    }
                } else if(changes == 2) {
                    // ????? ???????? ??? ???????
                    mt2_state_ctrl = MT2CTRL_WRFRAM;
                }
            }
            break;
            
        case MT2CTRL_RDFRAM:  // ?????? ?? FRAM
            pRxbuf = (uint8_t*)i2cFRAM_rd(next_addr_tm2, 1);
            if((uint32_t)pRxbuf > 0) {
                rd_symbol = pRxbuf[0];
                curr_addr_tm2 = next_addr_tm2;
                mt2_state_ctrl = MT2CTRL_PROC;
            }
            break;
            
        case MT2CTRL_WRFRAM:  // ?????? ? FRAM
            if((uint32_t)i2cFRAM_wr(curr_addr_tm2, &wr_symbol, 1) > 0) {
                if(next_symbol != 0) {
                    // ?????????? ?????? ?????? (??? ?????? ??????)
                    wr_symbol = next_symbol;
                    next_symbol = 0;
                    curr_addr_tm2--;
                    // ???????? ? ????????? ??????
                } else {
                    mt2_state_ctrl = MT2CTRL_RDFRAM;
                }
            }
            break;
            
        case MT2CTRL_UARTTR:  // ???????? ??????????
            if(uart_transmit(aTrbuf, strlen((char*)aTrbuf)) == 0) {
                mt2_state_ctrl = MT2CTRL_RXCMD;
            }
            break;
    }
}