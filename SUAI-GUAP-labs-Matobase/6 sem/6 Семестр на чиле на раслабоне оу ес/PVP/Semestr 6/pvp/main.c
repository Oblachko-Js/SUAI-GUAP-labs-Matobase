void SystemClock_Config(void);
void GPIO_Config(void);

int main(void) {
    SystemClock_Config();  // ????????? ????????? ???????
    GPIO_Config();         // ????????? GPIO
    
    // ????????? ??? 2250 ?? (?????? ~444 ???, ?????????? ~222 ???)
    const uint32_t half_period_2250 = 1776;  // ????????? ???????? ??? 8 ???
    
    // ????????? ??? 750 ?? (?????? ~1333 ???, ?????????? ~666 ???)
    const uint32_t half_period_750 = 5328;   // ????????? ???????? ??? 8 ???
    
    uint32_t counter_2250 = 0;  // ??????? ??? PA5
    uint32_t counter_750 = 0;   // ??????? ??? PA4
    uint8_t state_2250 = 0;     // ????????? PA5
    uint8_t state_750 = 0;      // ????????? PA4
    
    while (1) {
        // ????????? PA5 (2250 ??)
        if (counter_2250 >= half_period_2250) {
            if (state_2250) {
                GPIOA->BSRR = GPIO_BSRR_BR_5;  // ?????? ??????? PA5
                state_2250 = 0;
            } else {
                GPIOA->BSRR = GPIO_BSRR_BS_5;  // ??????? ??????? PA5
                state_2250 = 1;
            }
            counter_2250 = 0;
        }
        
        // ????????? PA4 (750 ??)
        if (counter_750 >= half_period_750) {
            if (state_750) {
                GPIOA->BSRR = GPIO_BSRR_BR_4;  // ?????? ??????? PA4
                state_750 = 0;
            } else {
                GPIOA->BSRR = GPIO_BSRR_BS_4;  // ??????? ??????? PA4
                state_750 = 1;
            }
            counter_750 = 0;
        }
        
        // ?????????? ?????????
        counter_2250++;
        counter_750++;
        
        // ????????? ???????? ??? ?????????? ????????
        //for(volatile uint32_t i = 0; i < 1; i++);  // ??????????? ????????
    }
}

void GPIO_Config(void) {
    // ???????? ???????????? ????? A
    RCC->AHBENR |= RCC_AHBENR_GPIOAEN;
    
    // ??????????? PA5 ??? ?????
    GPIOA->MODER &= ~GPIO_MODER_MODER5;
    GPIOA->MODER |= GPIO_MODER_MODER5_0;
    GPIOA->OTYPER &= ~GPIO_OTYPER_OT_5;
    GPIOA->OSPEEDR |= GPIO_OSPEEDER_OSPEEDR5_0;
    
    // ??????????? PA4 ??? ?????
    GPIOA->MODER &= ~GPIO_MODER_MODER4;
    GPIOA->MODER |= GPIO_MODER_MODER4_0;
    GPIOA->OTYPER &= ~GPIO_OTYPER_OT_4;
    GPIOA->OSPEEDR |= GPIO_OSPEEDER_OSPEEDR4_0;
}

void SystemClock_Config(void) {
    // ??????? ????????? ?? HSI 8 ???
    RCC->CR |= RCC_CR_HSION;
    while(!(RCC->CR & RCC_CR_HSIRDY));
    
    RCC->CFGR &= ~RCC_CFGR_SW;
    RCC->CFGR |= RCC_CFGR_SW_HSI;
    
    while((RCC->CFGR & RCC_CFGR_SWS) != RCC_CFGR_SWS_HSI);
}