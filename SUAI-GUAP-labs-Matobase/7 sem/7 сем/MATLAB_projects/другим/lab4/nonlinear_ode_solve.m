% Вариант №1. Решение нелинейного ДУ y' + 2x y = x e^{-x^2}, y(0)=2

% 1. Задаем временной интервал и начальные условия
tspan = [0 5];       % Интервал по x 
y0 = 2;               % Начальное условие y(0)=2   

% 2. Решаем ОДУ первого порядка с помощью решателя ode45
[t, y] = ode45(@odefun_nonlin, tspan, y0);

% 3. Строим график
figure;
plot(t, y, 'LineWidth', 2);
grid on;
title('Решение нелинейного ДУ: y'' + 2x y = x e^{-x^2}');
xlabel('x');
ylabel('y(x)');
legend('y(x)');

% 5. Функция, задающая правую часть ОДУ
function dydt = odefun_nonlin(t, y)
    % Здесь y — скаляр (значение функции в точке t=x)
    dydt = t*exp(-t^2) - 2*t*y;   % y' = x e^{-x^2} - 2x y
end