% Пункт 2. Решение системы линейных ДУ
% dx/dt = 4x - y
% dy/dt = x + 2y
% с начальными условиями x(0)=-1, y(0)=0

% 1. Задаем матрицу коэффициентов A
A = [4  -1;
     1   2];

% 2. Задаем временной интервал и начальные условия
tspan = [0 5];          % Время от 0 до 5
init_cond = [-1; 0];    % Вектор [x(0); y(0)]

% 3. Решаем систему ОДУ с помощью решателя ode45
[t, XY] = ode45(@(t, xy) A * xy, tspan, init_cond);

% 4. Извлекаем решения x(t) и y(t)
x = XY(:, 1);
y = XY(:, 2);

% 5. Строим графики
figure;
plot(t, x, 'b-', 'LineWidth', 2);
hold on;
plot(t, y, 'r--', 'LineWidth', 2);
grid on;
title('Решение системы линейных ДУ: dx/dt = 4x - y, dy/dt = x + 2y');
xlabel('Время t');
ylabel('x(t), y(t)');
legend('x(t)', 'y(t)');
hold off;

% 6. Дополнительный график для крупного плана в начале
figure;
plot(t, x, 'b-', t, y, 'r--', 'LineWidth', 2);
xlim([0 0.5]);          % Смотрим на начало процесса
grid on;
title('Начальный участок решения системы ДУ');
xlabel('Время t');
ylabel('x(t), y(t)');
legend('x(t)', 'y(t)');