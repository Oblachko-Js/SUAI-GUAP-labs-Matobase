% Пункт 2. Решение нелинейного ДУ y''*y^2 - 4*y' = 0, y(1)=1, y'(1)=-4

% 1. Задаем временной интервал и начальные условия
tspan = [1 5]; % Время от 1 до 5 секунд
y0 = [1; -4];  % Вектор начальных условий [y(1); y'(1)]

% 2. Решаем систему ОДУ с помощью решателя ode45
[t, Y] = ode45(@odefun_nonlin, tspan, y0);

% 3. Извлекаем решение y(t) из результата
y = Y(:, 1); % Первый столбец - это y(t)

% 4. Строим график
figure;
plot(t, y, 'LineWidth', 2);
grid on;
title('Решение нелинейного ДУ: y''''y^2 - 4y'' = 0');
xlabel('Время t');
ylabel('y(t)');
legend('y(t)');

% 5. Функция, задающая систему ОДУ для решателя
function dydt = odefun_nonlin(t, y)
    % y(1) = y
    % y(2) = y'
    dydt = zeros(2,1);
    dydt(1) = y(2); % y' = y2
    dydt(2) = (4 * y(2)) / (y(1)^2); % y'' = (4*y') / (y^2)
end